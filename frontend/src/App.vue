<template>
  <div id="app">
    {{ users }}
    <img alt="Vue logo" src="./assets/logo.png" />
    <HelloWorld msg="Welcome to Your Vue.js App" />
  </div>
</template>

<script>
import HelloWorld from './components/HelloWorld.vue';

export default {
  name: 'App',
  components: {
    HelloWorld,
  },
  data() {
    return { users: [] };
  },
  async created() {
    const response = await fetch('/api/users');
    this.users = await response.json();
  },
};
</script>

